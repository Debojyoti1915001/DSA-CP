#include <bits/stdc++.h>
using namespace std;
#define l long long int
void solve()
{
    l n;
    cin>>n;
    l steps=0;
    //0 1 2 3
    //
    while(steps*(steps+1)<2*n)steps++;
    if(steps*(steps+1)/2==n+1)steps++;
    cout<<steps<<endl;
}
 
int main()
{
    int t = 1;
    cin >> t;
    for (l i = 1; i <= t; i++)
    {
        //            cout<<"Case #"<<i<<": ";
        solve();
    }
    return 0;
}
 