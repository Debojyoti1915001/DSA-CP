#include <bits/stdc++.h>
using namespace std;
#define l long long int
void solve()
{
    l n;
    cin>>n;
    for(l i=n-3;i>1;i--)cout<<i<<" ";
    cout<<n-2<<" "<<1<<" "<<n-1<<" "<<n<<endl;;
}
 
int main()
{
    int t = 1;
    cin >> t;
    for (l i = 1; i <= t; i++)
    {
        //            cout<<"Case #"<<i<<": ";
        solve();
    }
    return 0;
}
 